<a name="Contattare un moderatore"></a><h3><img src="images/icons/mail.png" alt=" " /> Contattare un moderatore/amministratore:</h3>

<h4>Perchè contattare un moderatore o un amministratore:</h4>
<p>
I moderatori possono:<ul><li>Mettere o togliere pause, o perfino pattare in partite nelle quali un giocatore non potesse votare.</li>
<li>Accedere a mesi di log e al database per analizzare e scovare possibili multiaccounters, in base a diversi criteri.</li>
<li>Bannare utenti (e togliere il ban), resettare le password, concedere o togliere punti, cambiare le impostazioni delle partite come ad esempio la lunghezza delle fasi, etc. Per segnalazioni, usate le informazioni qui in basso per inviare una richiesta.</li>
</ul>
</p>

<h4>Dove mandare la segnalazione</h4>
<ul>
<li>Inviate una email a "admin CHIOCCIOLA webdiplomacy PUNTO it", e non ai loro indirizzi personali.</li>
</ul>

<h4>Cosa scrivere</h4>
<p>Cercate sempre di avere un buon motivo per effettuare una segnalazione, e non parlatene nel forum. Nella vostra segnalazione dovreste includere:
<ul>
	<li>In caso di richiesta generale (ad esempio una richiesta di rimozione pausa):</li>
	<ul><li>
	Utilizzate un titolo appropriato (ad esempio “Togliere la pausa” o “cambiare la durata dei turni”, in modo da permetterci di avere la percezione di quello che vi serve prima ancora di leggere la richiesta. In caso contrario, i tempi potrebbero allungarsi.</li>
	<li>Nelle situazioni quali mettere pause o votare la patta, è fondamentale raggiungere l'accordo con gli altri giocatori, quindi controllate prima che esprimano il loro consenso nella chat pubblica.</li>
	</ul>
	<li>In caso di segnalazione di multi accounting: 
	<ul>
    <li>
      Collegamento agli account dei giocatori che abbiano destato i vostri sospetti.
    </li>
    <li>
      Collegamento a qualsiasi partita che abbia destato I vostri sospetti.
    </li>
    <li>
      Qualsiasi informazione rilevante di cui abbiate notizia.
    </li>
    <li>
      Non siete costretti a dirci chi siete, ma può essere utile.
    </li>
    <li>Se effettuate una segnalazione via email, rendete chiara la situazione già nel titolo: piuttosto che “<i>Multi?</i>”, è molto meglio “<i>Multi: Utente1 e Utente2</i>”.</li>
    </ul></li>
</ul></p>
<p>Cercheremo di rispondervi il più rapidamente possibile, ma siate pazienti: le ricerche possono essere lunghe. Se dopo una settimana o due non avete notato cambiamenti della situazione, siete liberi di contattarci di nuovo.</p>

