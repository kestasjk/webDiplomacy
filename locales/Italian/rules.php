<a name="Diritti"></a><h3><img src="images/icons/tick.png" alt=" " /> Diritti:</h3>
<ul><li>I moderatori e gli amministratori cercano di far rispettare le regole, nel reciproco rispetto nei confronti dei giocatori.</li>
<li>I moderatori e gli amministratori mantengono un punto di vista obiettivo, quando devono far rispettare le regole.</li>
<li>I moderatori e gli amministratori concedono il beneficio del dubbio – ma fino ad un certo punto.</li>
<li>Le operazioni di moderazione sono registrate, e possono essere controllate qualora qualcuno ne faccia richiesta.</li>
</ul>

