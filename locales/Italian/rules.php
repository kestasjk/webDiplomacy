<a name="Regole"></a><h3><img src="images/icons/alert.png" alt=" " /> Regole per giocare a Diplomacy:</h3>
<h4>1. Nel Diplomacy è vietato il multiaccount</h4>
<p>
  Potete avere <em>un solo account</em>, un secondo account non è permesso <em>per nessuna ragione</em>, e verrà  bannato. Anche il vostro primo account potrebbe venire bannato, a seconda delle circostanze. Se avete dimenticato la Vostra password, provate <a class="light" href="logon.php?forgotPassword=1">qui</a>.  Se ancora non riuscite, contattate i moderatori. 
</p>

<h4>2. E’ vietato il meta-gaming</h4>
<p>
  Non potete fare alleanze per motivi <em>non inerenti al gioco</em>, come amicizie, parentele, o per favori pregressi in altre partite. Questo è conosciuto come metagaming ed è contro le regole perchè dà un vantaggio scorretto ai giocatori coinvolti. Se avete problemi nel tradire qualcuno perchè non volete rovinare l'amicizia per noi va bene, purchè allora non vi iscriviate in una stessa partita.
</p>

<h4>3. Non aggirate le regole sui messaggi di gioco.</h4>
<p>
 Se entrate in una partita con condizioni limitate di comunicazione (ad esempio, solo diplomazia pubblica), allora non potete usare altri metodi di comunicazione come email o messaggi privati. 
</p>

<h4>3.1 Rispettate l'anonimato</h4>
<p>
Se entrate in una partita anonima (in particolare se è presente diplomazia) non rivelate in nessun caso la vostra identità ad altri utenti del sito.
A seconda della gravità dell'infrazione gli admin procederanno alla rimozione dalla partita, alla sospensione dal sito, fino al ban - e valuteranno la cancellazione della partita se l'infrazione coinvolge un gran numero di utenti.</p>

<h4>4. Non sfruttate moderatori e software a proprio vantaggio diplomatico</h4>
<p>
  Le bugie sono le benvenute nel Diplomacy –<em>tranne quando procurano lavoro non necessario ai moderatori e agli amministratori!</em>  <br /> <br/>

Accusare mendacemente qualcuno di essere un multiaccounter solo per radunare il tavolo contro di lui, o dichiarare che le mosse eseguite non sono quelle inserite a mo di scusa, significa coinvolgere moderatori ed admin nella ricerca di bug o imbrogli che non esistono realmente.
Qualsiasi accusa nei confronti di altri giocatori DEVE essere portata all’attenzione esclusiva dei moderatori e degli admin, come scritto più avanti, <em>in privato</em>, ed un’affermazione di errore del software <em>deve essere sincera</em>, anche se dichiarata ad un altro giocatore.

</p>

<h4>5. Il Pause/Unpause non è lì per essere sfruttato diplomaticamente</h4>
<p>
  Lo scopo della pausa è di non far saltare l’immissione degli ordini agli altri giocatori, <em>ma non fa parte del gioco</em>. Quindi, mettere o togliere la pausa non deve essere sfruttato per la propria diplomazia (ad esempio rifiutandosi di togliere la pausa per costringere gli altri alla patta). Se ci sono abusi lo staff ha facoltà  di intervenire.
</p>

<h4>6. Non si può accedere all’account di un altro giocatore senza il suo permesso</h4>
<p>
  In generale, è permesso ed incoraggiato l'account sitting (permettere a qualcuno di fiducia di collegarsi al posto vostro per dare gli ordini quando voi non potete).<br/> Tuttavia, non è permesso concedere l'accesso a qualcuno con il quale si condividono partite. Inoltre, qualsiasi azione effettuata dal vostro sostituto è vostra responsabilità, quindi siate convinti di quello che fate prima di dare in giro la vostra password.
  Anche <em>cambiare la password</em> dopo aver ripreso in mano il proprio account è vostra responsabilità.
</p>
<h4>7. Niente spam, sul forum o nei messaggi privati</h4>
<p>
  Siete invitati ed incoraggiati a divertirvi all’interno della comunità, ma con rispetto. Cercate di dare risposte pertinenti alle conversazioni nelle quali intervenite, e non aprite troppe discussioni alla volta.
</p>
<h4>8. Rispondete alle richieste dei moderatori e degli amministratori</h4>
<p>
  Dovete rispondere a qualsiasi messaggio o avvertimento inviato dai moderatori, perché in caso di mancata risposta si assume che abbiate qualcosa da nascondere, con eventuali ripercussioni sul vostro account. Per questo, <em>siate sicuri</em> di controllare regolarmente l’indirizzo email specificato, perché lo staff userà prevalentemente quello per comunicare con voi.
</p>
<h4>9. Seguite il buon senso</h4>
<p>
  Le regole specificate finora sono principalmente regole non ovvie. Anche se uno specifico comportamento contrario al buon senso non dovesse comparire nelle regole di cui sopra, questo non vuol dire che i moderatori non possano intervenire in difesa del divertimento altrui!
</p>

