<a name="ppscwta"></a>
<h4>Diverse modalità di divisione dei punti: <em>Punti-per-Numero-di-Centri</em> oppure <em>Vincitore-Intasca-la-Posta</em></h4>
<div align="center">
	<img src="images/points/wta.png" alt=" "
		title="Partita VIP: in caso di vittoria, il vincitore è l'unico a ricevere i punti; tutti quelli in palio" />
</div>
<p class="intro">
Per i giocatori più esperti c'è la modalità VIP ( Vincitore Intasca la Posta), che può essere scelta al momento della creazione della partita. In questa modalità il vincitore si prende tutti i punti in palio e gli altri giocatori non hanno niente.
<br />Questa modalità è stata pensata apposta per chi crede che non ci sia onore nell'essere secondi e che bisogna sempre puntare alla vittoria. 
<br /><br />
Ma ricorda che in questa modalità è più probabile rimanere a secco alla fine. Anche se giochi bene, potresti non rivedere indietro i tuoi punti, quindi cerca di scommettere di meno nelle partite VIP rispetto alle classiche con la divisione per numero di centri.
</p>

<h4>Patta</h4>
<div align="center">
	<img src="images/points/draw.png" alt=" "
		title="Quando tutti i giocatori ancora attivi votano per la patta, la posta in palio viene divisa in parti uguali tra tutti i sopravissuti" />
</div>
<p class="intro">
A volte la partita si blocca; le unità sono disposte in modo tale da impedire ad un altro giocatore di vincere,
 e nessuno vuole cambiare strategia. In questi casi si arriva ad una patta e i punti sono equamente divisi tra tutti i sopravissuti
 indipendentemente dal numero di centri di ciascuno. <br /><br />

La patta può essere votata in qualsiasi momento; se tutti i giocatori ancora in partita votano per la patta,
 la partita finisce e i punti vengono divisi tra tutti (in una patta il giocatore con 15 centri riceve lo stesso numero di punti
 di un giocatore con un solo centro!).</p>

<a name="minpoints"></a>
<h4>Soglia minima dei punti</h4>
<p class="intro">
I nuovi giocatori hanno solo 100 punti all'inizio, ma non si devono preoccupare di perderli tutti!
 Se una partita finisce ed il giocatore, incluse tutte le altre scommesse dei partite ancora in corso, ha meno di 100 punti,
 riceve indietro la scommessa della partita appena conclusa.
</p>

<h4>Disciplina!</h4>
<p class="intro">
A mano a mano che i giocatori vincono sempre più punti, possono entrare in partite che costano sempre di più. 
Ma le partite più impegnative in termini di punti sono anche le più rischiose. 
Puoi vincere o perdere somme elevate molto in fretta, quindi fai attenzione a scommettere tutti i tuoi punti! <br />
Se hai tanti punti cerca di investirli in altre partite con saggezza, o ti ritroverai di nuovo a giocare con i novellini!
</p>
